package korea.it.culture.vo;

public class LocInfoVO {

  private String locId, locName, locTel, locUrl, locAddr, locLa, locLo, locSeatscale;

  public String getLocId() {
    return locId;
  }

  public void setLocId(String locId) {
    this.locId = locId;
  }

  public String getLocName() {
    return locName;
  }

  public void setLocName(String locName) {
    this.locName = locName;
  }

  public String getLocTel() {
    return locTel;
  }

  public void setLocTel(String locTel) {
    this.locTel = locTel;
  }

  public String getLocUrl() {
    return locUrl;
  }

  public void setLocUrl(String locUrl) {
    this.locUrl = locUrl;
  }

  public String getLocAddr() {
    return locAddr;
  }

  public void setLocAddr(String locAddr) {
    this.locAddr = locAddr;
  }

  public String getLocLa() {
    return locLa;
  }

  public void setLocLa(String locLa) {
    this.locLa = locLa;
  }

  public String getLocLo() {
    return locLo;
  }

  public void setLocLo(String locLo) {
    this.locLo = locLo;
  }

  public String getLocSeatscale() {
    return locSeatscale;
  }

  public void setLocSeatscale(String locSeatscale) {
    this.locSeatscale = locSeatscale;
  }
}
